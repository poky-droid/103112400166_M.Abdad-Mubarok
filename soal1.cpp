#include <iostream>

using namespace std;

int main() {
   int input1, input2;
   cin >> input1 >> input2;
    cout << "penjumlahan: " ;
    cout << input1 + input2 << endl;
    cout << "pengurangan: " ;
    cout << input1 - input2 << endl;
    cout << "perkalian: " ;
    cout << input1 * input2 << endl;
    cout << "pembagian: " ;
    cout << input1 / input2 << endl;
   return 0;
}
